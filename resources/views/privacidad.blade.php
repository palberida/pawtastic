        <h2>1. Quiénes somos</h2>
        <p>
            ossu es una tienda en línea con sede en Guatemala. Operamos el sitio web
            <a href="https://ossu.gt">ossu.gt</a> y atendemos a clientes principalmente a través
            de WhatsApp y redes sociales (Facebook e Instagram). El responsable del tratamiento
            de tus datos es ossu, contactable en
            <a href="mailto:ventas@ossu.gt">ventas@ossu.gt</a>.
        </p>

        <h2>2. Qué datos recopilamos</h2>
        <p>Cuando interactúas con ossu por WhatsApp o haces clic en uno de nuestros anuncios, podemos recopilar:</p>
        <ul>
            <li>Tu número de teléfono de WhatsApp.</li>
            <li>El nombre que tengas configurado en tu perfil de WhatsApp.</li>
            <li>El contenido de los mensajes que nos envías (texto, imágenes, audios, etc.).</li>
            <li>Información del anuncio que originó la conversación (por ejemplo, identificador del anuncio y producto promocionado), proporcionada por Meta.</li>
            <li>Marcas de tiempo y metadatos técnicos de los mensajes recibidos.</li>
        </ul>
        <p>
            Si concretas una compra, también recopilamos los datos necesarios para procesar y
            entregar tu pedido (nombre completo, dirección de entrega, detalles de pago y
            preferencias de envío).
        </p>

        <h2>3. Para qué usamos tus datos</h2>
        <ul>
            <li>Responder a tus consultas y brindarte atención al cliente.</li>
            <li>Procesar pedidos, coordinar envíos y gestionar pagos.</li>
            <li>Mejorar la calidad de nuestros productos, anuncios y atención.</li>
            <li>Cumplir con obligaciones legales, contables y fiscales aplicables en Guatemala.</li>
        </ul>
        <p>
            No usamos tus mensajes de WhatsApp para fines distintos a la atención comercial
            relacionada con ossu, y no enviamos publicidad masiva no solicitada por WhatsApp.
        </p>

